defmodule Explorer.Chain.Log.Schema do
  @moduledoc false
  use Utils.CompileTimeEnvHelper,
    chain_identity: [:explorer, :chain_identity]

  alias Explorer.Chain.{
    Address,
    Block,
    CompressedData,
    Data,
    Hash,
    Transaction
  }

  alias Explorer.Utility.AddressIdToAddressHash

  # In certain situations, like on Polygon, multiple logs may share the same
  # index within a single block due to a RPC node bug. To prevent system crashes
  # due to not unique primary keys, we've included `transaction_hash` in the
  # primary key.
  #
  # However, on Celo, logs may exist where `transaction_hash` equals block_hash.
  # In these instances, we set `transaction_hash` to `nil`. This action, though,
  # violates the primary key constraint. To resolve this issue, we've excluded
  # `transaction_hash` from the composite primary key when dealing with
  # `:optimism-celo` chain type.
  #
  # UPD: `transaction_hash` migrated to `transaction_index` but logic above remains the same
  @transaction_field (case @chain_identity do
                        {:optimism, :celo} ->
                          quote do
                            [
                              field(:transaction_index, :integer)
                            ]
                          end

                        _ ->
                          quote do
                            [
                              field(:transaction_index, :integer, primary_key: true)
                            ]
                          end
                      end)

  defmacro generate do
    quote do
      @primary_key false
      typed_schema "logs" do
        field(:data, Data)
        field(:compressed_data, CompressedData)
        field(:first_topic, Hash.Full)
        field(:second_topic, Hash.Trimmed)
        field(:third_topic, Hash.Trimmed)
        field(:fourth_topic, Hash.Trimmed)
        field(:index, :integer, primary_key: true, null: false)
        field(:block_number, :integer, primary_key: true)

        timestamps()

        belongs_to(:address_mapping, AddressIdToAddressHash,
          foreign_key: :address_id,
          references: :address_id,
          type: :integer
        )

        has_one(:address, through: [:address_mapping, :address])

        # TODO: remove after migration to address id is done
        belongs_to(
          :address_by_hash,
          Address,
          foreign_key: :address_hash,
          references: :hash,
          type: Hash.Address
        )

        belongs_to(:block, Block,
          foreign_key: :block_hash,
          references: :hash,
          type: Hash.Full
        )

        belongs_to(:transaction, Transaction,
          foreign_key: :transaction_hash,
          references: :hash,
          type: Hash.Full
        )

        unquote_splicing(@transaction_field)
      end
    end
  end
end

