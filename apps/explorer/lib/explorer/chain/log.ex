  end

  # Returns a query to get all logs with consensus blocks.
  #
  # ## Returns
  # - A query to be used by other Repo functions.
  @spec get_known_block_number_query() :: Ecto.Query.t()
  defp get_known_block_number_query do
    from(
      log in __MODULE__,
      inner_join: block in Block,
      on: block.number == log.block_number and block.consensus == true,
      select: log
    )
  end

  @doc """
  Filters logs by one or multiple topic fields.

  When `topic_name` is an atom, filters the query by the given topic field and
  values. `topic_values` can be a single value or a list of values.

  When `topic_names` and `topic_values_list` are lists, builds an `OR` condition
  where each topic field is matched against the corresponding list of values.
  """
  @spec filter_by_topic_query(Ecto.Queryable.t(), atom() | [atom()], any() | [[any()]]) :: Ecto.Query.t()
  def filter_by_topic_query(query, topic_name, topic_values) when is_atom(topic_name) do
    where(query, [l], ^topic_filter_dynamic(topic_name, List.wrap(topic_values)))
  end

  def filter_by_topic_query(query, topic_names, topic_values_list)
      when is_list(topic_names) and is_list(topic_values_list) do
    where(query, [l], ^filter_by_topic_dynamic(topic_names, topic_values_list))
  end

  def filter_by_topic_dynamic(topic_names, topic_values_list) do
    topic_names
    |> Enum.zip(topic_values_list)
    |> Enum.reduce(dynamic(false), fn {topic_name, topic_values}, dynamic ->
      dynamic(
        [l],
        ^dynamic or ^topic_filter_dynamic(topic_name, topic_values)
      )
    end)
  end

  def topic_filter_dynamic(:first_topic, topic_values), do: dynamic([l], l.first_topic in ^topic_values)

  def topic_filter_dynamic(topic_name, topic_values) do
    if LogHelper.fill_optimized_fields_migration_finished?() do
      dynamic([l], field(l, ^topic_name) in ^topic_values)
    else
      extended_values =
        topic_values
        |> Enum.flat_map(fn topic ->
          # credo:disable-for-next-line Credo.Check.Refactor.Nesting
          case Hash.Full.dump(topic) do
            {:ok, extended} -> [extended]
            :error -> []
          end
        end)

      dynamic(
        [l],
        field(l, ^topic_name) in ^topic_values or field(l, ^topic_name) in type(^extended_values, {:array, :binary})
      )
    end
  end

  defp by_transaction_query(query, transaction_hash, block_number, transaction_index) do
    cond do
      LogHelper.fill_optimized_fields_migration_finished?() ->
        where(query, [l], l.block_number == ^block_number and l.transaction_index == ^transaction_index)

      LogHelper.fill_optimized_fields_migration_started?() ->
        where(
          query,
          [l],
          l.transaction_hash == ^transaction_hash or
            (l.block_number == ^block_number and l.transaction_index == ^transaction_index)
        )

      true ->
        where(query, [l], l.transaction_hash == ^transaction_hash)
    end
  end

  @doc """
  Joins transactions to the given logs query.
  """
  @spec join_transaction_query(Ecto.Queryable.t()) :: Ecto.Query.t()
  def join_transaction_query(query \\ __MODULE__) do
    cond do
      LogHelper.fill_optimized_fields_migration_finished?() ->
        join(query, :inner, [l], t in Transaction,
          on: l.block_number == t.block_number and l.transaction_index == t.index,
          as: :transaction
        )

      LogHelper.fill_optimized_fields_migration_started?() ->
        join(query, :inner, [l], t in Transaction,
          on: l.transaction_hash == t.hash or (l.block_number == t.block_number and l.transaction_index == t.index),
          as: :transaction
        )

      true ->
        join(query, :inner, [l], t in Transaction, on: l.transaction_hash == t.hash, as: :transaction)
    end
  end

  @doc """
  Joins logs to the given token transfers query.
  """
  @spec join_to_token_transfer_query(Ecto.Queryable.t()) :: Ecto.Query.t()
  # credo:disable-for-next-line Credo.Check.Refactor.CyclomaticComplexity
  def join_to_token_transfer_query(query) do
    cond do
      LogHelper.fill_optimized_fields_migration_finished?() ->
        query
        |> join(:left, [tt], t in assoc(tt, :transaction))
        |> join(:left, [tt, t], l in __MODULE__,
          on: tt.block_number == l.block_number and t.index == l.transaction_index and tt.log_index == l.index,
          as: :log
        )

      LogHelper.fill_optimized_fields_migration_started?() ->
        query
        |> join(:left, [tt], t in assoc(tt, :transaction))
        |> join(:left, [tt, t], l in __MODULE__,
          on:
            ((tt.block_number == l.block_number and t.index == l.transaction_index) or
               tt.transaction_hash == l.transaction_hash) and tt.log_index == l.index,
          as: :log
        )

      true ->
        join(query, :left, [tt], l in __MODULE__,
          on: tt.block_hash == l.block_hash and tt.transaction_hash == l.transaction_hash and tt.log_index == l.index,
          as: :log
        )
    end
  end

  @doc """
  Preloads consensus blocks for log records.

  When a list of logs is provided and `blocks` is `nil`, the function fetches
  consensus blocks by `block_number` and attaches each block to the matching log.

  It also ensures that `:block_hash` is populated from the loaded block when
  available, while preserving the existing value if no block is found.
  """
  def join_address_mapping_query(query, join_type \\ :left) do
    with_named_binding(query, :address_mapping, fn query, binding ->
      join(query, join_type, [it], m in AddressIdToAddressHash,
        as: ^binding,
        on: it.address_id == m.address_id
      )
    end)
  end

  def address_match_query(query, address_hashes) when is_list(address_hashes) do
    cond do
      LogHelper.fill_optimized_fields_migration_finished?() ->
        address_ids = AddressIdToAddressHash.hashes_to_ids(address_hashes)
        where(query, [l], l.address_id in ^address_ids)

      LogHelper.fill_optimized_fields_migration_started?() ->
        address_ids = AddressIdToAddressHash.hashes_to_ids(address_hashes)
        where(query, [l], l.address_id in ^address_ids or l.address_hash in ^address_hashes)

      true ->
        where(query, [l], l.address_hash in ^address_hashes)
    end
  end

  def address_match_query(query, address_hash) do
    cond do
      LogHelper.fill_optimized_fields_migration_finished?() ->
        address_id = AddressIdToAddressHash.hash_to_id(address_hash)
        where(query, [l], ^address_id_match_dynamic(address_id))

      LogHelper.fill_optimized_fields_migration_started?() ->
        address_id = AddressIdToAddressHash.hash_to_id(address_hash)
        where(query, [l], ^address_id_or_hash_match_dynamic(address_id, address_hash))

      true ->
        where(query, [l], l.address_hash == ^address_hash)
    end
  end

  defp address_id_match_dynamic(nil), do: dynamic(false)
  defp address_id_match_dynamic(address_id), do: dynamic([l], l.address_id == ^address_id)

  defp address_id_or_hash_match_dynamic(nil, address_hash), do: dynamic([l], l.address_hash == ^address_hash)

  defp address_id_or_hash_match_dynamic(address_id, address_hash),
    do: dynamic([l], l.address_id == ^address_id or l.address_hash == ^address_hash)

  @spec preload_block(map() | [map()] | nil, module(), [Block.t()] | nil) :: __MODULE__.t() | [__MODULE__.t()] | nil
  def preload_block(logs, repo \\ Repo, blocks \\ nil)

  def preload_block(nil, _repo, _blocks), do: nil
  def preload_block([], _repo, _blocks), do: []

  def preload_block(logs, repo, nil) when is_list(logs) do
    blocks_map =
      logs
      |> Enum.map(& &1.block_number)
      |> Block.by_numbers_query()
      |> Block.consensus_query(true)
      |> repo.all()
      |> Map.new(&{&1.number, &1})

    Enum.map(logs, fn log ->
      block = blocks_map[log.block_number]
      Map.merge(log, %{block_hash: (block && block.hash) || log.block_hash, block: block})
    end)
  end

  def preload_block(logs, _repo, existing_blocks) when is_list(logs) do
    blocks_map = Map.new(existing_blocks, &{&1.number, &1})

    Enum.map(logs, fn log ->
      block = blocks_map[log.block_number]
      Map.merge(log, %{block_hash: (block && block.hash) || log.block_hash, block: block})
    end)
  end

  def preload_block(log, repo, existing_blocks) do
    [log]
    |> preload_block(repo, existing_blocks)
    |> List.first()
  end

  @doc """
  Preloads transactions for log records.

  When a list of logs is provided and `transactions` is `nil`,
  the function fetches the corresponding parent transactions by
  `{block_number, transaction_index}` and attaches each transaction to the
  `:transaction` field of the matching log.

  It also ensures that `:transaction_hash` is populated from the loaded
  transaction when available, while preserving the existing value if no
  transaction is found.

  ## Parameters
  - `logs`: A single log, a list of logs, or `nil`
  - `repo`: The repo used to fetch transactions when they are not passed explicitly
  - `transactions`: Optional preloaded transactions to reuse instead of
    querying the database

  ## Returns
  - `nil` when `logs` is `nil`
  - A list of logs with the `:transaction` field populated
  - A single log with the `:transaction` field populated
  """
  @spec preload_transaction(map() | [map()] | nil, [any()], module(), [Transaction.t()] | nil) ::
          __MODULE__.t() | [__MODULE__.t()] | nil
  def preload_transaction(logs, preloads \\ [], repo \\ Repo, transactions \\ nil)

  def preload_transaction(nil, _preloads, _repo, _transactions), do: nil
  def preload_transaction([], _preloads, _repo, _transactions), do: []

  def preload_transaction(logs, preloads, repo, existing_transactions) when is_list(logs) do
    transactions =
      case existing_transactions do
        nil ->
          logs
          |> Enum.map(&{&1.block_number, &1.transaction_index})
          |> Enum.uniq()
          |> Transaction.by_block_number_index_query()
          |> repo.all()
          |> repo.preload(preloads)

        transactions ->
          List.wrap(transactions)
      end

    block_number_index_to_transaction_map = Map.new(transactions, &{{&1.block_number, &1.index}, &1})

    Enum.map(logs, fn it ->
      transaction = block_number_index_to_transaction_map[{it.block_number, it.transaction_index}]

      Map.merge(it, %{
        transaction: transaction,
        transaction_hash: (transaction && transaction.hash) || it.transaction_hash
      })
    end)
  end

  def preload_transaction(log, preloads, repo, transactions) do
    [log]
    |> preload_transaction(preloads, repo, transactions)
    |> List.first()
  end

  @spec preload_address(map() | [map()] | nil, Keyword.t(), module()) :: __MODULE__.t() | [__MODULE__.t()] | nil
  def preload_address(logs, options \\ [], repo \\ Repo)

  def preload_address(nil, _options, _repo), do: nil
  def preload_address([], _options, _repo), do: []

  def preload_address(logs, options, repo) when is_list(logs) do
    preloads = [address: Keyword.get(options, :address_preloads, [])]
    repo = repo || Chain.select_repo(options)

    indexed_logs = Enum.with_index(logs)

    {migrated_indexed, not_migrated_indexed} =
      Enum.split_with(indexed_logs, fn {l, _idx} -> is_nil(l.address_hash) end)

    migrated = Enum.map(migrated_indexed, &elem(&1, 0))
    not_migrated = Enum.map(not_migrated_indexed, &elem(&1, 0))

    not_migrated_preloaded = preload_addresses_for_not_migrated_logs(not_migrated, preloads, repo)
    migrated_preloaded = preload_addresses_for_migrated_logs(migrated, preloads, repo)

    migrated_with_idx = Enum.zip(migrated_preloaded, Enum.map(migrated_indexed, &elem(&1, 1)))
    not_migrated_with_idx = Enum.zip(not_migrated_preloaded, Enum.map(not_migrated_indexed, &elem(&1, 1)))

    (migrated_with_idx ++ not_migrated_with_idx)
    |> Enum.sort_by(&elem(&1, 1))
    |> Enum.map(&elem(&1, 0))
  end

  def preload_address(log, options, repo) do
    [log]
    |> preload_address(options, repo)
    |> List.first()
  end

  @spec prepare_data(map() | [map()] | nil) :: __MODULE__.t() | [__MODULE__.t()] | nil
  def prepare_data(nil), do: nil

  def prepare_data(logs) when is_list(logs) do
    Enum.map(logs, &%{&1 | data: &1.data || &1.compressed_data})
  end

  def prepare_data(log) do
    [log]
    |> prepare_data()
    |> List.first()
  end

  defp preload_addresses_for_not_migrated_logs([], _preloads, _repo), do: []

  defp preload_addresses_for_not_migrated_logs(logs, preloads, repo) do
    unified_preloads =
      preloads
      |> List.wrap()
      |> Enum.map(fn
        :address -> {:address_by_hash, []}
        {:address, fields} -> {:address_by_hash, fields}
      end)

    logs
    |> repo.preload(unified_preloads)
    |> Enum.map(fn log ->
      address = Map.get(log, :address_by_hash)

      Map.merge(log, %{
        address_hash: (address && address.hash) || Map.get(log, :address_hash),
        address: address
      })
    end)
  end

  defp preload_addresses_for_migrated_logs([], _preloads, _repo), do: []

  defp preload_addresses_for_migrated_logs(logs, preloads, repo) do
    logs
    |> repo.preload(preloads)
    |> Enum.map(fn log ->
      address = Map.get(log, :address)
      Map.put(log, :address_hash, address && address.hash)
    end)
  end
